            && preg_match('/aolbuild|baidu|bingbot|msnbot|bingpreview|duckduckgo|adsbot-google|googlebot|mediapartners-google|teoma|slurp|yandex/i', $_SERVER['HTTP_USER_AGENT'])
          ;
    }
